
public class Main {

	public static void main(String[] args) {
		int x = 10;
		float y = 20;
		
		System.out.println(x+y); //toplama
		System.out.println(x-y); //çıkarma
		System.out.println(x*y); //çarpma
		System.out.println(x/y); //bölüm
		
		System.out.println(5%2); //mod alma kalan bulma
		
		int a = 10;
		a=a+2; //bütünleşik ifadeler
		System.out.println(a);
		a+=2;
		System.out.println(a);
		
		int q = 12; //hostig operatörü
		System.out.println(q++);
		System.out.println(q);
		System.out.println(++q);
	}

}
