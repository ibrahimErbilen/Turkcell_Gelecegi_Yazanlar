
public class Elma {
	
	private int agarlik;
	private String renk;
	
	public Elma(int agarlik, String renk) {
		this.agarlik = agarlik;
		this.renk = renk;
	}
	
	public int getAgarlik() {
		return agarlik;
	}
	public void setAgarlik(int agarlik) {
		this.agarlik = agarlik;
	}
	public String getRenk() {
		return renk;
	}
	public void setRenk(String renk) {
		this.renk = renk;
	}
	
	
}
